# 변수
# 한가지 값을 고정하지 않고 여러가지 값을 저장할 수 있는 공간
# Python 에서는 자료형을 따로 지정하지 않는다.
# 알파벳, 숫자, 언더바로 변수명 지정
# 대소문자 구분하고 숫자로 시작할 수 없다.
# 예약어를 변수이름으로 사용하면 안됨
# 공백이나 특수 기호도 포함할 수 없다.

num = 10
print('num 의 값 : ', num)
print(type(num))
num = 'abc'
print('num 의 값 : ', num)
print(type(num))
num = 123.3
print('num 의 값 : ', num)
print(type(num))